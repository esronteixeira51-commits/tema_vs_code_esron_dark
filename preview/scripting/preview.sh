#!/usr/bin/env bash

###############################################################################
# Esron Dark - Bash Preview
# Este arquivo existe apenas para testar a coloração do tema.
###############################################################################

set -euo pipefail

#==============================================================================
# Constants
#==============================================================================

readonly APP_NAME="Esron Dark"

readonly VERSION="1.0.0"

readonly PI=3.14159

#==============================================================================
# Variables
#==============================================================================

username="Esron"

age=25

enabled=true

directory="/home/esron/projects"

file="preview.txt"

count=0

#==============================================================================
# Arrays
#==============================================================================

languages=(
    "Python"
    "Rust"
    "Go"
    "Zig"
    "C++"
)

numbers=(1 2 3 4 5)

#==============================================================================
# Associative Arrays
#==============================================================================

declare -A colors=(
    [keyword]="blue"
    [type]="red"
    [string]="green"
    [number]="orange"
)

#==============================================================================
# Functions
#==============================================================================

print_header() {
    echo "========================================="
    echo "$APP_NAME"
    echo "Version: $VERSION"
    echo "========================================="
}

sum() {
    local a=$1
    local b=$2

    echo $((a + b))
}

greet() {
    local name="$1"

    printf "Hello %s\n" "$name"
}

#==============================================================================
# Command Substitution
#==============================================================================

today=$(date)

current_dir=$(pwd)

kernel=$(uname -r)

user=$(whoami)

#==============================================================================
# Arithmetic
#==============================================================================

result=$((5 + 10))

power=$((2 ** 8))

mod=$((25 % 4))

#==============================================================================
# String Expansion
#==============================================================================

message="Esron Dark Theme"

echo "${message}"

echo "${message^^}"

echo "${message,,}"

echo "${message:0:6}"

echo "${#message}"

#==============================================================================
# Conditions
#==============================================================================

if [[ $enabled == true ]]; then
    echo "Enabled"
else
    echo "Disabled"
fi

#==============================================================================
# Case
#==============================================================================

case "$1" in

    start)
        echo "Starting..."
        ;;

    stop)
        echo "Stopping..."
        ;;

    restart)
        echo "Restarting..."
        ;;

    *)
        echo "Unknown command"
        ;;

esac

#==============================================================================
# Loops
#==============================================================================

for lang in "${languages[@]}"; do
    echo "$lang"
done

for ((i=0;i<5;i++)); do
    echo "$i"
done

while ((count < 5)); do
    echo "$count"
    ((count++))
done

#==============================================================================
# Files
#==============================================================================

if [[ -f "$file" ]]; then
    cat "$file"
fi

if [[ -d "$directory" ]]; then
    ls "$directory"
fi

#==============================================================================
# Test Operators
#==============================================================================

[[ -r "$file" ]]

[[ -w "$file" ]]

[[ -x "$file" ]]

[[ -e "$file" ]]

#==============================================================================
# Pipes
#==============================================================================

ps aux | grep bash

ls -la | head

echo "Hello" | tr '[:lower:]' '[:upper:]'

#==============================================================================
# Here Document
#==============================================================================

cat <<EOF

Esron Dark
Bash Preview

EOF

#==============================================================================
# ANSI Colors
#==============================================================================

RED="\033[31m"

GREEN="\033[32m"

BLUE="\033[34m"

RESET="\033[0m"

printf "${RED}Red${RESET}\n"

printf "${GREEN}Green${RESET}\n"

printf "${BLUE}Blue${RESET}\n"

#==============================================================================
# Trap
#==============================================================================

cleanup() {
    echo "Cleaning..."
}

trap cleanup EXIT

#==============================================================================
# Builtins
#==============================================================================

echo "Hello"

printf "World\n"

read -r line <<< "Example"

declare variable

export PATH

unset variable

alias ll='ls -la'

type echo

help printf

#==============================================================================
# Exit Codes
#==============================================================================

if grep "bash" /etc/shells >/dev/null 2>&1; then
    echo "Installed"
fi

#==============================================================================
# Functions
#==============================================================================

print_header

greet "$username"

echo "2 + 3 = $(sum 2 3)"

#==============================================================================
# End
#==============================================================================

exit 0