SELECT
    id,
    username,
    email
FROM users;

/*==============================================================================
  Where
==============================================================================*/

SELECT *

FROM users

WHERE active = TRUE

AND id > 1

OR username LIKE 'E%';

/*==============================================================================
  Order
==============================================================================*/

SELECT *

FROM users

ORDER BY username ASC;

/*==============================================================================
  Group
==============================================================================*/

SELECT

    active,

    COUNT(*) AS total

FROM users

GROUP BY active;

/*==============================================================================
  Join
==============================================================================*/

SELECT

    u.username,

    p.title

FROM users u

INNER JOIN posts p

ON u.id = p.user_id;

/*==============================================================================
  Left Join
==============================================================================*/

SELECT *

FROM users u

LEFT JOIN posts p

ON u.id = p.user_id;

/*==============================================================================
