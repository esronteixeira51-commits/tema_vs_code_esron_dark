package preview.kotlin

import kotlin.math.PI
import kotlin.random.Random

//=============================================================================
// Constants
//=============================================================================

const val VERSION = "1.0.0"

//=============================================================================
// Enum
//=============================================================================

enum class Status {
    PENDING,
    RUNNING,
    FINISHED
}

//=============================================================================
// Data Class
//=============================================================================

data class User(

    val id: Int,

    var name: String,

    var active: Boolean = true

)

//=============================================================================
// Sealed Class
//=============================================================================

sealed class Result {

    data class Success(val message: String) : Result()

    data class Error(val exception: Exception) : Result()

    object Loading : Result()

}

//=============================================================================
// Object
//=============================================================================

object Config {

    const val THEME = "Esron Dark"

}

//=============================================================================
// Interface
//=============================================================================

interface Logger {

    fun log(message: String)

}

//=============================================================================
// Class
//=============================================================================

class Service : Logger {

    override fun log(message: String) {

        println(message)

    }

}

//=============================================================================
// Extension Function
//=============================================================================

fun String.capitalizeWords(): String {

    return split(" ")

        .joinToString(" ") {

            it.replaceFirstChar(Char::uppercase)

        }

}

//=============================================================================
// Generic Class
//=============================================================================

class Box<T>(

    val value: T

)

//=============================================================================
// Functions
//=============================================================================

fun add(a: Int, b: Int): Int {

    return a + b

}

fun area(radius: Double): Double {

    return PI * radius * radius

}

//=============================================================================
// Higher Order Function
//=============================================================================

fun execute(

    action: () -> Unit

) {

    action()

}

//=============================================================================
// Main
//=============================================================================

fun main() {

    val version = VERSION

    var count = 0

    val user = User(

        id = 1,

        name = "Esron"

    )

    println(user)

    val box = Box("Kotlin")

    println(box.value)

    println(add(5, 10))

    println(area(5.0))

    //-------------------------------------------------------------------------
    // Collections
    //-------------------------------------------------------------------------

    val languages = listOf(

        "Kotlin",

        "Java",

        "Rust",

        "Go"

    )

    val mutable = mutableListOf(

        1,

        2,

        3

    )

    val map = mapOf(

        "theme" to "Esron Dark",

        "version" to version,

        "enabled" to true

    )

    val set = setOf(

        "A",

        "B",

        "C"

    )

    //-------------------------------------------------------------------------
    // Loops
    //-------------------------------------------------------------------------

    for (language in languages) {

        println(language)

    }

    for (i in 1..5) {

        println(i)

    }

    while (count < 5) {

        count++

    }

    //-------------------------------------------------------------------------
    // If
    //-------------------------------------------------------------------------

    if (user.active) {

        println("Active")

    } else {

        println("Inactive")

    }

    //-------------------------------------------------------------------------
    // When
    //-------------------------------------------------------------------------

    when (user.id) {

        1 -> println("Admin")

        else -> println("Guest")

    }

    //-------------------------------------------------------------------------
    // Nullable
    //-------------------------------------------------------------------------

    var nullable: String? = null

    println(nullable ?: "Default")

    nullable = "Hello"

    println(nullable.length)

    //-------------------------------------------------------------------------
    // Scope Functions
    //-------------------------------------------------------------------------

    user.apply {

        name = "Developer"

    }

    user.let {

        println(it.name)

    }

    //-------------------------------------------------------------------------
    // Lambda
    //-------------------------------------------------------------------------

    val multiply = { a: Int, b: Int ->

        a * b

    }

    println(multiply(4, 5))

    execute {

        println("Higher Order Function")

    }

    //-------------------------------------------------------------------------
    // Exception
    //-------------------------------------------------------------------------

    try {

        throw Exception("Example")

    } catch (e: Exception) {

        println(e.message)

    } finally {

        println("Finished")

    }

    //-------------------------------------------------------------------------
    // Random
    //-------------------------------------------------------------------------

    println(Random.nextInt())

    //-------------------------------------------------------------------------
    // String Templates
    //-------------------------------------------------------------------------

    println("User: ${user.name}")

    println("Version: $version")

    //-------------------------------------------------------------------------
    // Range
    //-------------------------------------------------------------------------

    val range = 1..10

    println(range)

    //-------------------------------------------------------------------------
    // Destructuring
    //-------------------------------------------------------------------------

    val (id, name, active) = user

    println(id)

    println(name)

    println(active)

    //-------------------------------------------------------------------------
    // Result
    //-------------------------------------------------------------------------

    val result: Result = Result.Success("OK")

    when (result) {

        is Result.Success -> println(result.message)

        is Result.Error -> println(result.exception)

        Result.Loading -> println("Loading")

    }

    //-------------------------------------------------------------------------
    // Extension Function
    //-------------------------------------------------------------------------

    println("esron dark".capitalizeWords())
}