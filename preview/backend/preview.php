// Match
//-----------------------------------------------------------------------------

$status = Status::Running;

$result = match ($status) {

    Status::Pending => "Pending",

    Status::Running => "Running",

    Status::Finished => "Finished",

};

echo $result . PHP_EOL;

//-----------------------------------------------------------------------------
// If
//-----------------------------------------------------------------------------

if ($enabled) {

    echo "Enabled";

} else {

    echo "Disabled";

}

//-----------------------------------------------------------------------------
// For
//-----------------------------------------------------------------------------

for ($i = 0; $i < 5; $i++) {

    echo $i;

}

//-----------------------------------------------------------------------------
// While
//-----------------------------------------------------------------------------

$count = 0;

while ($count < 5) {

    $count++;

}

//-----------------------------------------------------------------------------
// Arrow Function
//-----------------------------------------------------------------------------

$square = fn(int $x): int => $x * $x;

echo $square(5);

//-----------------------------------------------------------------------------
