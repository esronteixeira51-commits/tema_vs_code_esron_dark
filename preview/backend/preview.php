namespace Preview;

use DateTime;
use Exception;
use RuntimeException;

//=============================================================================
// Constants
//=============================================================================

const VERSION = '1.0.0';

//=============================================================================
// Enum
//=============================================================================

enum Status: string
{
    case Pending = 'pending';
    case Running = 'running';
    case Finished = 'finished';
}

//=============================================================================
// Trait
//=============================================================================

trait Logger
{
    public function log(string $message): void
    {
        echo "[LOG] {$message}\n";
    }
}

//=============================================================================
// Interface
//=============================================================================

interface Printable
{
    public function print(): void;
}

//=============================================================================
// Attribute
//=============================================================================

#[Attribute]
class Example
{
    public function __construct(
        public string $name
    ) {
    }
}

//=============================================================================
// Class
//=============================================================================

#[Example("User")]

class User implements Printable
{
    use Logger;

    public function __construct(
        public readonly int $id,
        public string $name,
        public bool $active = true
    ) {
    }

    public function greet(): void
    {
        $this->log("Hello {$this->name}");
    }

    public function print(): void
    {
        echo "{$this->id} {$this->name}\n";
    }
}

//=============================================================================
// Function
//=============================================================================

function add(int $a, int $b): int
{
    return $a + $b;
}

//=============================================================================
// Generator
//=============================================================================

function numbers(): Generator
{
    for ($i = 1; $i <= 5; $i++) {
        yield $i;
    }
}

//=============================================================================
// Main
//=============================================================================

$user = new User(
    id: 1,
    name: "Esron"
);

$user->greet();

echo add(10, 20) . PHP_EOL;

//-----------------------------------------------------------------------------
// Variables
//-----------------------------------------------------------------------------

$name = "Esron";

$version = VERSION;

